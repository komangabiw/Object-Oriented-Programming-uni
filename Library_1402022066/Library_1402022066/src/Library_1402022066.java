    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(userRegist.getText().length() > 0){
            if(passRegist.getText().length() > 0){
                if(passRegist.getText().length() < 8){ 
                    JOptionPane.showMessageDialog(null, "Password minimal 8 karakter",
                        "Coba Lagi", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    if("Daftar Sebagai".equals("" + rolesRegist.getSelectedItem())){
                       JOptionPane.showMessageDialog(null, "Kamu belum pilih role",
                        "Coba Lagi", JOptionPane.INFORMATION_MESSAGE); 
                    }else{
                        if(cekRegist(userRegist.getText())){
                            JOptionPane.showMessageDialog(null, "Username ini telah digunakan",
                            "Warning", JOptionPane.ERROR_MESSAGE);
                        }else{
                            String sql = "insert into data(username, password, roles) values (?, ?, ?)";
                            try{
                                dbConnect.pst = dbConnect.con.prepareStatement(sql);
                                dbConnect.pst.setString(1, userRegist.getText());
                                dbConnect.pst.setString(2, passRegist.getText());
                                dbConnect.pst.setString(3, "" + rolesRegist.getSelectedItem());
                                dbConnect.pst.executeUpdate();
                                JOptionPane.showMessageDialog(this, "Akun Berhasil Dibuat");
                                //action reset semua yg udah diketik dan klik
                                userRegist.setText("");
                                passRegist.setText("");
                                rolesRegist.setSelectedIndex(0);
                                Register.setVisible(false);
                                Login.setVisible(true);
                                checkRegist.setSelected(false);
                            }catch(HeadlessException | SQLException ex){
                                JOptionPane.showMessageDialog(this, ex.getMessage());               
                            }
                        }
                    }
                } 
            }else{
                JOptionPane.showMessageDialog(null, "Kamu belum input pasword",
                     "Coba Lagi", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Kamu belum input username",
                "Coba Lagi", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


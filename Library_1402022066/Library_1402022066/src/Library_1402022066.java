    public boolean cekLogin(String username, String password, String roles){
        String sqlcon = "select * from data";
                
        try{
            ResultSet cek = dbConnect.con.createStatement().executeQuery(sqlcon);
            while(cek.next()){
                if(username.equals(cek.getString("username")) && password.equals(cek.getString("password")) && roles.equals(cek.getString("roles"))){
                    return true;
                }
            }
        }catch(SQLException ex){
            System.out.print(ex.toString());
        }
        return false;
    }
    
    public boolean cekRegist(String usernameRegist){
        String sqlcon = "select * from data";
                
        try{
            ResultSet cek = dbConnect.con.createStatement().executeQuery(sqlcon);
            while(cek.next()){
                if(usernameRegist.equals(cek.getString("username"))){
                    return true;
                }
            }
        }catch(SQLException ex){
            System.out.print(ex.toString());
        }
        return false;
    }
    
